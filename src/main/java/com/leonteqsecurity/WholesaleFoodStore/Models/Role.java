package com.leonteqsecurity.WholesaleFoodStore.Models;

/**
 * Enumeration representing roles in the Wholesale Food Store application.
 * This enum defines roles such as 'Admin' and 'User'.
 */
public enum Role {

    /**
     * The 'Admin' role represents administrative privileges.
     */
    Admin,

    /**
     * The 'User' role represents regular user privileges.
     */
    User
}
